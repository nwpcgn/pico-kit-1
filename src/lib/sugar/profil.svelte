<h2 id="profil">Profil</h2>
<p>
	Als der geborene Gentleman-Agen (wenn auch mit einer leichten Neigung zu
	Cocktails und Chaos) hat Archer schon alles gesehen: Schurken, Geheimnisse und
	unangenehme Familienessen. Mit einer Schusswaffe so präzise wie seine scharfen
	Kommentare und einem Stil, der selbst im Kugelhagel nie leidet, ist er die
	perfekte Wahl, wenn Diskretion und Dramatik gleichermaßen gefragt sind.
</p>
